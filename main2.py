    print("  Ваше число больше или меньше моего? (больше/меньше)")
    min = 1
    max = 100
    answer = str(input())
    while True:
        if answer == "угадал":
            print("  Ура!")
            break
        else:
            if max - min != 0:
                if answer == "больше":
                    if max == var:
                        print("  Вы наверное ошиблись. Повторите пожалуйста.")
                        print(f"  Я думаю, что это число {var}.")
                    else:
                        min = var+1
                        var = random.randint(min, max)
                        print(f"  Я думаю, что это число {var}.")
                if answer == "меньше":
                    if min == var:
                        print("  Вы наверное ошиблись. Повторите пожалуйста.")
                        print(f"  Я думаю, что это число {var}.")
                    else:
                        max = var-1
                        var = random.randint(min, max)
                        print(f"  Я думаю, что это число {var}.")
            else:
                print(f"  Я уверен, это число {var}.")
                break

        answer = str(input())
    sleep(1)

