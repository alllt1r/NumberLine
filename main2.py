import random
from time import sleep

def FirstGame():
    var = random.randint(1, 100)
    print("  Я загадал число от 1, до 100. Ваша задача - угадать его.")
    while True:
        try:
            last = int(input())
            if last == var:
                print("  Поздравляю, вы угадали!")
                break
            if last > var:
                print("  Ваше число больше задуманного.")
            if last < var:
                print("  Ваше число меньше задуманного.")
        except: print("  Ваше число может содержать ТОЛЬКО цифры.")

    sleep(1)

def SecondGame():
    var = random.randint(1, 100)
    print("  Загадайте число от 1 до 100. Моя задача - угадать его")
    sleep(1.5)
